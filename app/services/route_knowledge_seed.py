HIGH_RISK_NAMES = {"贡嘎大环线", "梅里雪山外转徒步路线", "太白山南北穿越", "鳌太穿越"}
BLOCKED_NAMES = {"鳌太穿越"}


def starter_routes() -> list[dict[str, object]]:
    records: list[dict[str, object]] = []
    for province, routes in PROVINCE_ROUTES.items():
        for index, (name, city, tags) in enumerate(routes):
            official = OFFICIAL_ROUTES.get(name)
            official_status = official[0] if official else "unverified"
            sources = [
                {
                    "title": "OpenStreetMap 地图检索",
                    "url": f"https://www.openstreetmap.org/search?query={quote(name)}",
                    "source_type": "open_map",
                }
            ]
            if official:
                sources.insert(0, {"title": official[1], "url": official[2], "source_type": "official"})
            risk_level = "blocked" if name in BLOCKED_NAMES else "high" if name in HIGH_RISK_NAMES else "normal"
            records.append(
                {
                    "id": f"seed-{province}-{index + 1:02d}",
                    "name": name,
                    "province": province,
                    "city": city,
                    "aliases": _aliases_for(name),
                    "difficulty": "进阶" if any(tag in {"进阶", "穿越", "高风险"} for tag in tags) else None,
                    "tags": list(tags),
                    "seasons": _seasons_for(tags),
                    "camping": True if "露营" in tags else None,
                    "editorial_rank": 3 if index < 5 else 2 if index < 10 else 1,
                    "official_status": official_status,
                    "risk_level": risk_level,
                    "risk_notes": ["该路线存在较高户外风险，必须核对属地公告并由具备经验者评估。"] if risk_level == "high" else [],
                    "status": "blocked" if risk_level == "blocked" else "active",
                    "last_verified_at": None,
                    "sources": sources,
                }
            )
    return records


def _aliases_for(name: str) -> list[str]:
    aliases: list[str] = []
    for suffix in ("徒步路线", "徒步线路", "登山路线", "步道"):
        if name.endswith(suffix) and len(name) > len(suffix) + 1:
            aliases.append(name[: -len(suffix)])
    return aliases


def _seasons_for(tags: tuple[str, ...]) -> list[str]:
    seasons: list[str] = []
    if "红叶" in tags:
        seasons.append("秋季")
    if "花海" in tags:
        seasons.extend(["春季", "夏季"])
    if "候鸟" in tags:
        seasons.append("冬季")
    return seasons
